/**
  * @brief  在FFT频谱中查找局部最大值
  * @param  spectrum: 幅度谱数组
  * @param  centerBin: 理论频率对应的bin索引
  * @param  searchRange: 搜索范围(±)
  * @param  maxBin: 频谱中的最大bin数
  * @retval 局部最大值的幅度
  */
float FR_FindLocalMaximum(float *spectrum, uint32_t centerBin, uint32_t searchRange, uint32_t maxBin)
{
    float maxVal = 0.0f;

    // 计算搜索起始bin，确保不小于0
    // 如果centerBin > searchRange，则从(centerBin - searchRange)开始
    // 否则从0开始，避免数组越界
    uint32_t startBin = (centerBin > searchRange) ? (centerBin - searchRange) : 0;

    // 计算搜索结束bin，确保不超过最大bin
    // 如果(centerBin + searchRange) < maxBin，则到(centerBin + searchRange)结束
    // 否则到(maxBin - 1)结束，避免数组越界
    uint32_t endBin = (centerBin + searchRange < maxBin) ? (centerBin + searchRange) : maxBin - 1;

    // 在指定范围内搜索最大值
    for (uint32_t i = startBin; i <= endBin; i++) {
        if (spectrum[i] > maxVal) {
            maxVal = spectrum[i];
        }
    }

    return maxVal;
}

/**
  * @brief  处理采样数据 (使用实FFT优化)
  * @param  frequency: 当前测量的频率,0-2495对应1K:200:500K
  * @param  samples: 采样数据缓冲区
  * @param  sampleNum: 采样点数
  * @param  sampleRate: 采样率
  * @retval None
  */
void FR_ProcessSamples(uint32_t frequency, uint16_t *samples, uint32_t sampleNum, uint32_t sampleRate)
{
    // 计算1.5V直流偏置对应的ADC值
    uint16_t dc_offset_adc = (uint16_t)((VDC_OFFSET / VREF) * (ADC_RESOLUTION-1));
    float k = 0 ;
    if(frequency<=20) // 幅值补偿
    {
    	k = 1 ;
    }else{

    	k = 4.3898285 ;
    }
    // 清空FFT输入缓冲区
    for (uint32_t i = 0; i < sampleNum; i++) {
        fft_input_buf[i] = 0.0f;
    }

    // 将ADC采样转换为浮点数并放入FFT输入缓冲区，同时减去直流偏置
    for (uint32_t i = 0; i < sampleNum; i++) {
        // 减去直流偏置，转换为浮点数
        if (samples[i] >= dc_offset_adc) {
            fft_input_buf[i] = (float)(samples[i] - dc_offset_adc) / (ADC_RESOLUTION-1)*3.3;
        } else {
            fft_input_buf[i] = -(float)(dc_offset_adc - samples[i]) / (ADC_RESOLUTION-1)*3.3;
        }
    }

    // 如果频率超过阈值，应用Kaiser窗，20对应5K;
    if (frequency >20) {
        FR_ApplyKaiserWindow(fft_input_buf, sampleNum);
    }

    // 执行实FFT
    if (sampleNum == 512) {
        arm_rfft_fast_f32(&rfft_instance_512, fft_input_buf, fft_output_buf, 0);
    } else if (sampleNum == 4096) {
        arm_rfft_fast_f32(&rfft_instance_4096, fft_input_buf, fft_output_buf, 0);
    }

    // 计算幅度谱 (实FFT输出结构: [real0, imag0, real1, imag1, ...])
    // 由于是实信号输入，我们需要计算复数的幅度
    float32_t magnitudes[sampleNum/2];

    for (uint32_t i = 0; i < sampleNum/2; i++) {
        float32_t real = fft_output_buf[2*i];
        float32_t imag = fft_output_buf[2*i+1];
        magnitudes[i] = sqrtf(real*real + imag*imag) / sampleNum*k*2; //记得幅值的补偿
    }

    // 找到基频对应的FFT bin
    uint32_t bin_index = (uint32_t)((float)(frequency*200+1000) * sampleNum / sampleRate + 0.5f);
    // 使用局部最大值搜索算法找到实际的幅度峰值
    float magnitude = FR_FindLocalMaximum(magnitudes, bin_index, 2, sampleNum/2);
    // 获取幅度值并归一化
    magnitude = magnitude/1.12f;
    // 近似处理,可能是不可取的，看情况，对频谱的测量做一定补偿
    //magnitude = FR_ThresholdMagnitude(magnitude);

    // 保存频率点和对应的幅度
    if (response_count < MAX_RESPONSE_POINTS) {
        freq_array[response_count] = frequency;
        magnitude_array[response_count] = magnitude;
        // 相位会在单独的函数中测量
        response_count++;
    }
}
/**
  * @brief  测量特定频率的相位特性
  * @param  frequency: 测量频率(Hz)
  * @retval None
  */
void FR_MeasurePhase(uint32_t frequency)
{
    // 在实际项目中，此处应从ADC获取乘法器输出的直流量
	// ADC3读取

    // 从ADC读取反映相位的直流值
    HAL_ADC_Start(&hadc3);
    HAL_ADC_PollForConversion(&hadc3, 100);
    uint32_t phaseADCValue = HAL_ADC_GetValue(&hadc3);

    // 将ADC值转换为相位角度(0到pi)
    // 电压越大，相位差的越多，但要注意这个东西要归一化。
    float phase = ((float)phaseADCValue / (ADC_RESOLUTION-1)) *3.1416f  ;

    // 存储相位数据
    if (response_count > 0 && response_count <= MAX_RESPONSE_POINTS) {
        phase_array[response_count-1] = phase;  // 关联到上次测量的频率点
    }
}

/**
  * @brief  测量整个频率范围的幅频响应
  * @param  None
  * @retval None
  */
void FR_MeasureResponse(void)
{
    uint32_t sampleNum, sampleRate;

    // 清空响应数据
    response_count = 0;

    // 遍历所有频率点
    for (uint32_t freq = 0; freq <= 2495; freq += 1) {
        // 配置频率扫描参数
    	uint32_t timerPeriod = 0 ;
        if (freq <= 20) {
            sampleNum = LOW_FREQ_SAMPLE_NUM;
            sampleRate = LOW_FREQ_SAMPLE_RATE;
            timerPeriod = 624 ;
            // 设置定时器周期以匹配采样率

                 __HAL_TIM_SET_AUTORELOAD(&htim2, timerPeriod);

                 // 生成特定频率的正弦波
                 FR_GenerateSineWave(freq);

                 // 清除完成标志
                 adc_complete_flag = 0;

                 // 启动ADC采样，使用DMA (单次模式)
                 HAL_ADC_Start_DMA(&hadc1, (uint32_t*)adc_buffer1, sampleNum);

                 // 启动定时器触发ADC采样
                 HAL_TIM_Base_Start(&htim2);

                 // 等待ADC采样完成
                 while (!adc_complete_flag)
                	 {
                	  ;
                	 };
                 // 仅停止定时器，DMA和ADC在单次模式下会自动停止
                 HAL_TIM_Base_Stop(&htim2);
                 // ADC和DMA已自动停止，定时器在中断中停止

                 // 处理采样数据
                 FR_ProcessSamples(freq, adc_buffer1, sampleNum, sampleRate);

                 // 测量相位特性
                 FR_MeasurePhase(freq);

        } else {
            sampleNum = HIGH_FREQ_SAMPLE_NUM;
            sampleRate = HIGH_FREQ_SAMPLE_RATE;
            timerPeriod = 49 ;
            // 设置定时器周期以匹配采样率

                 __HAL_TIM_SET_AUTORELOAD(&htim2, timerPeriod);

                 // 生成特定频率的正弦波
                 FR_GenerateSineWave(freq);

                 // 清除完成标志
                 adc_complete_flag = 0;

                 // 启动ADC采样，使用DMA (单次模式)
                 HAL_ADC_Start_DMA(&hadc1, (uint32_t*)adc_buffer2, sampleNum);

                 // 启动定时器触发ADC采样
                 HAL_TIM_Base_Start(&htim2);

                 // 等待ADC采样完成
                 while (!adc_complete_flag) {
                    ;
                 }
                 // 仅停止定时器，DMA和ADC在单次模式下会自动停止
                 HAL_TIM_Base_Stop(&htim2);
                 // 处理采样数据
                 FR_ProcessSamples(freq, adc_buffer2, sampleNum, sampleRate);

                 // 测量相位特性
                 FR_MeasurePhase(freq);
        }



    }


}



/**
  * @brief  对幅度谱进行二值化处理
  * @param  magnitude: 原始幅度值
  * @retval 二值化后的幅度值
  */
float FR_ThresholdMagnitude(float magnitude)
{
    // 二值化阈值
    const float HIGH_THRESHOLD = 0.95f;
    const float LOW_THRESHOLD = 0.01f;

    // 二值化处理
    if (magnitude > HIGH_THRESHOLD) {
        return 1.0f;  // 通带
    } else if (magnitude < LOW_THRESHOLD) {
        return 0.0f;  // 阻带
    } else {
        return magnitude;  // 过渡带保持原值
    }
}




int FR_DetermineRLCType(void)
{
    if (response_count < 10) {
        return RLC_UNKNOWN; // 数据点太少，无法判断
    }

    // 找到最大和最小幅度值及其对应的频率
    float maxMag = 0.0f, minMag = 1.0f;
    uint32_t maxFreq = 0, minFreq = 0;
    float firstMag = magnitude_array[0];
    float lastMag = magnitude_array[response_count-1];

    for (uint32_t i = 0; i < response_count; i++) {
        if (magnitude_array[i] > maxMag) {
            maxMag = magnitude_array[i];
            maxFreq = freq_array[i];
        }
        if (magnitude_array[i] < minMag) {
            minMag = magnitude_array[i];
            minFreq = freq_array[i];
        }
    }

    // 判断电路类型
    // 低通: 低频通过，高频衰减
    if (firstMag > 0.7f && lastMag < 0.3f) {
        return RLC_LOWPASS;
    }
    // 高通: 高频通过，低频衰减
    else if (firstMag < 0.3f && lastMag > 0.7f) {
        return RLC_HIGHPASS;
    }
    // 带通: 中间频率通过，两端衰减
    else if (maxMag > 0.7f && firstMag < 0.3f && lastMag < 0.3f) {
        return RLC_BANDPASS;
    }
    // 带阻: 中间频率衰减，两端通过
    else if (minMag < 0.3f && firstMag > 0.7f && lastMag > 0.7f) {
        return RLC_BANDSTOP;
    }

    return RLC_UNKNOWN;
}



